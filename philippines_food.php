				  <h2><li>Chicken Adobo</h2>
				  <img src = "images/chickenAdobo.jpg" alt = "chicken adobo image" width = "100%" height = "100%">
				  </li>
				  <p>Chicken adobo is one of the most famous foods to try in the Philippines, known and loved by everyone. 
				  Adobo means “marinade” in Spanish and this is just that: chicken (or pork) marinated in a mixture of soya sauce and vinegar. 
				  Other ingredients are added depending on whose recipe you follow.</p>
				  
				  <h2><li>Balut - Duck Embryo</h2>
				  <img src = "images/balut.jpg" alt = "balut image" width = "100%" height = "100%">
				  </li>
				  <p>Balut is a developing duck embryo boiled and eaten as a snack in the shell and with a splash of vinegar. This is definitely one of the most famous foods to try in the Philippines.
				  Balut is a popular street food, which originated in the Philippines and is also frequently found in Vietnam, Laos and Cambodia.</p>
                  
				  <h2><li>Kare Kare - Oxtail Stew</h2>
				  <img src = "images/karekare.jpg" alt = "kare kare image" width = "100%" height = "100%">
				  </li>
				  <p>Kare kare is an oxtail and ox tripes stew with lots of vegetables, flavoured with ground roasted peanuts or peanut butter, onions and garlic. 
				  It is served with shrimp sauce (‘bagoong’), sometimes spiced with chill and sprinkled with calamansi juice (Filipino lime).
				  Kare Kare is famous throughout the whole country. The alleged origin of its name is from the word ‘curry’ due to the influence of the Indian community in Metro Manila’s Cainta, Rizal area.</p>
                  
				  <h2><li>Kinilaw - Raw Fish Salad</h2>
				  <img src = "images/kinilaw.jpg" alt = "kinilaw image" width = "100%" height = "100%">
				  </li>
				  <p>Kinilaw is similar to the famous Peruvian dish called ceviche. It is a raw fish salad served in an acidic juice, usually kalamansi (Filipino lime) and vinegar, which “cooks” the meat. 
				  Kinilaw comes from the word, “kilaw” which means, ‘eaten fresh’.
				  Other ingredients usually include garlic, ginger, onion, pepper and chilli. </p>
                  
				  <h2><li>Sinigang - Sour Meat Stew</h2>
				  <img src = "images/sinigang.jpg" alt = "sinigang image" width = "100%" height = "100%">
				  </li>
				  <p> Sinigang is a meat based sour stew or soup with lots of vegetables. The most popular souring agent is tamarind (sampalok). 
				  Pork (baboy) is the most common meat used but chicken, beef and fish are also popular.
				  Alternative souring agents include guava, tomatoes or kalamansi (Filipino lime).</p>
                  
				  <h2><li>Paksew Na Lechon</h2>
				  <img src = "images/pawsew.jpg" alt = "paksew image" width = "100%" height = "100%">
				  </li>
				  <p>Lechon means ‘suckling pig’ in Spanish and is literally a whole pig roasted over charcoal for many hours for special occasions. 
				  This is considered the national dish of the Philippines and the city of Cebu is considered one of the most famous places to eat it.
				   The leftovers of the lechon are stewed with vinegar and spices and become a delicious dish called paksiw na lechon. Paksiw literally means: to cook and simmer with vinegar.</p>
                  
				  <h2><li>Tapsilog - Cured Beef</h2>
				  <img src = "images/tapsilog.jpg" alt = "tapsilog image" width = "100%" height = "100%">
				  </li>
				  <p>Taspsilog is a famous Filipino breakfast, which refers to the contents comprising the meal. In this case, cured beef (tapa), fried rice (sinangag) and a fried egg (itlog).
				  Variants include ‘adosilog’ (adobo with fried rice and fried egg), litsilog (lechon with fried rice and egg). </p>
                  
				  <h2><li>Halo Halo</h2>
				  <img src = "images/halohalo.jpg" alt = "halohalo image" width = "100%" height = "100%">
				  </li>
				  <p>This is THE famous Filipino desert. 
				  Halo Halo (meaning ‘mixed together’ in Filipino) is served in a tall glass containing ice shavings, evaporated milk and various small chunks of yummy goodies all mixed in together.
				  Yummy goodies include boiled kidney beans, chickpeas, sugar palm fruit, jackfruit, tapioca, sweet potato, sweet beans, coconut gel, ice cream, guava paste, purple yam and many more!</p>
                  
				  <h2><li>Buko - Philippine Coconut</h2>
				  <img src = "images/buko.jpg" alt = "buko image" width = "100%" height = "100%">
				  </li>
				  <p>Buko juice is a popular, refreshing drink from the Philippines. "Buko" is a Filipino word that means "young coconut." Hence, Buko juice is also known as coconut milk drink. 
				  There are two ways to prepare a Buko juice, one that uses fresh juice from the coconut fruit and one that uses canned Buko juice concentrate.</p>
                  
